// Write a Java program to display the first 10 natural numbers.

public class Program12_NaturalNumbers {
    public static void main(String[] args) {
        int num = 1;
        System.out.println("First 10 Natural Numbers are :-");
        while (num <= 10) {
            System.out.print(num + " ");
            num++;
        }
    }
}
